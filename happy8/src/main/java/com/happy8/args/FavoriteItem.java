package com.happy8.args;

/**
 * 收藏列表项
 */
public class FavoriteItem {
	private int favId;
	private int clubId;
	public int getFavId() {
		return favId;
	}
	public void setFavId(int favId) {
		this.favId = favId;
	}
	public int getClubId() {
		return clubId;
	}
	public void setClubId(int clubId) {
		this.clubId = clubId;
	}
	
}
